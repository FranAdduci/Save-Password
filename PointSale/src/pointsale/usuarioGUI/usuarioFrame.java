    //Funcion que retoma lo que esta guardado en la tabla a los campos de texto
    private void tblUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuarioMouseClicked
        //Primero se verifica si se dio el "Click"
        //evt=evento
        if(evt.getClickCount()== 1){
            //Si da click
            //Jtable clase receptor y casteo(formato que necesito)
            JTable receptor = (JTable)evt.getSource();
            
            txtId.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),0).toString());
            txtUsuario.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),1).toString());
            txtPw.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),2).toString());
            txtEmail.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),3).toString());
        }
        btnAgregar.setEnabled(false);
        btnCancelar.setEnabled(true);
        btnEditar.setEnabled(true);
        btnBorrar.setEnabled(true);
        
    }//GEN-LAST:event_tblUsuarioMouseClicked

