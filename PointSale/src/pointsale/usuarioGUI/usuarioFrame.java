    public void mostrarDatos (){
        
        while(modelo.getRowCount()>0){
            modelo.removeRow(0);
        }       
        
        conectBD con = new conectBD();
        //con.getConexion();
        try {
            ResultSet resultado = con.consultarRegistros("SELECT * FROM usuario");

            while (resultado.next()) {
                System.out.println(resultado.getString("id_usr"));
                System.out.println(resultado.getString("nombre_usr"));
                System.out.println(resultado.getString("pw_usr"));
                System.out.println(resultado.getString("email_usr"));
                Object [] objUsr = {resultado.getString("id_usr"),resultado.getString("nombre_usr"),resultado.getString("pw_usr"),resultado.getString("email_usr")};
                
                modelo.addRow(objUsr);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        
        
    }
    public Usuario recuperarDatosGUIusr() {
        Usuario objUsr = new Usuario();

        int idUsr = (txtId.getText().isEmpty())?0 : Integer.parseInt(txtId.getText());
        objUsr.setIdUsr(idUsr);
        
        objUsr.setNameUsr(txtUsuario.getText());
        objUsr.setPwUsr(txtPw.getText());
        objUsr.setEmailUsr(txtEmail.getText());   
        return  objUsr;
    }
    
